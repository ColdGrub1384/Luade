-- Welcome to Luade!

-- Luade is a Lua IDE.

-- You can start by creating a script by tapping the "+" button on the main screen. You can then type your code and run it by tapping the Play button.

-- You can also code inside the REPL (Main Screen > Tab bar > REPL)

-- You can include all scripts in your current directory.

-- You can use all libraries, like `os` for example:

print(os.clock())

-- You can request for input:

io.write("What's your name? ")
name = io.read()
print("Hello "..name.."!")
